package it.polito.tdp.itunes.model;

public class Arco {
	
	private Integer source;
	private Integer target;
	
	public Arco(Integer source, Integer target) {
		this.source = source;
		this.target = target;
	}

	public Integer getSource() {
		return source;
	}

	public Integer getTarget() {
		return target;
	}
	
	
	

}
